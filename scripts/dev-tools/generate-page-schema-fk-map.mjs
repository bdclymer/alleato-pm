#!/usr/bin/env node

/**
 * Generate FK targets for Dev Tools page-schema registry from Supabase types.
 *
 * Source: frontend/src/types/database.types.ts
 * Output: frontend/src/components/dev-tools/page-schema-fk.generated.ts
 *
 * This keeps FK target labels in the Dev Tools panel aligned with live schema
 * after `npm run db:types`.
 */

import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const repoRoot = path.join(__dirname, "..", "..");
const typesPath = path.join(repoRoot, "frontend", "src", "types", "database.types.ts");
const outputPath = path.join(
  repoRoot,
  "frontend",
  "src",
  "components",
  "dev-tools",
  "page-schema-fk.generated.ts",
);

const TRACKED_KEYS = [
  "change_event_line_items.budget_code_id",
  "change_event_line_items.vendor_id",
];

function fail(message) {
  console.error(`[dev-tools fk sync] ${message}`);
  process.exit(1);
}

function extractTablesSection(content) {
  const tablesStart = content.indexOf("Tables: {");
  if (tablesStart < 0) fail("Could not find `Tables: {` in database types.");

  const viewsStart = content.indexOf("\n    Views: {", tablesStart);
  if (viewsStart < 0) fail("Could not find `Views: {` after `Tables: {`.");

  return content.slice(tablesStart, viewsStart);
}

function parseTableNames(tablesSection) {
  const names = new Set();
  const tableRegex = /^ {6}([a-zA-Z0-9_]+): \{$/gm;
  let match;
  while ((match = tableRegex.exec(tablesSection)) !== null) {
    names.add(match[1]);
  }
  return names;
}

function parseForeignKeys(tablesSection, tableNames) {
  const keyToRelations = new Map();
  const tableRegex = /^ {6}([a-zA-Z0-9_]+): \{\n([\s\S]*?)^ {6}\}$/gm;
  let tableMatch;

  while ((tableMatch = tableRegex.exec(tablesSection)) !== null) {
    const tableName = tableMatch[1];
    const tableBody = tableMatch[2];
    const relationshipsMatch = tableBody.match(/Relationships:\s*\[([\s\S]*?)\n {8}\]/m);
    if (!relationshipsMatch) continue;

    const relationshipsBody = relationshipsMatch[1];
    const relRegex =
      /\{[\s\S]*?columns:\s*\["([^"]+)"\][\s\S]*?referencedRelation:\s*"([^"]+)"[\s\S]*?referencedColumns:\s*\["([^"]+)"\][\s\S]*?\}/g;

    let relMatch;
    while ((relMatch = relRegex.exec(relationshipsBody)) !== null) {
      const column = relMatch[1];
      const referencedRelation = relMatch[2];
      const referencedColumn = relMatch[3];
      const key = `${tableName}.${column}`;
      const value = `${referencedRelation}.${referencedColumn}`;

      if (!keyToRelations.has(key)) keyToRelations.set(key, []);
      keyToRelations.get(key).push({
        value,
        referencedRelation,
        isRealTable: tableNames.has(referencedRelation),
      });
    }
  }

  return keyToRelations;
}

function chooseBestRelation(relations) {
  const realTable = relations.find((r) => r.isRealTable);
  if (realTable) return realTable.value;
  return relations[0]?.value ?? null;
}

function buildTrackedFkMap(keyToRelations) {
  const result = {};

  for (const key of TRACKED_KEYS) {
    const relations = keyToRelations.get(key) ?? [];
    const chosen = chooseBestRelation(relations);
    if (!chosen) fail(`Could not resolve FK target for tracked key: ${key}`);
    result[key] = chosen;
  }

  return result;
}

function writeOutputFile(fkMap) {
  const generatedAt = new Date().toISOString();
  const sortedEntries = Object.entries(fkMap).sort(([a], [b]) => a.localeCompare(b));

  const lines = [];
  lines.push("// AUTO-GENERATED FILE. DO NOT EDIT.");
  lines.push("// Generated by: scripts/dev-tools/generate-page-schema-fk-map.mjs");
  lines.push(`// Generated at: ${generatedAt}`);
  lines.push("");
  lines.push("export const PAGE_SCHEMA_FK_TARGETS: Record<string, string> = {");
  for (const [key, value] of sortedEntries) {
    lines.push(`  "${key}": "${value}",`);
  }
  lines.push("};");
  lines.push("");

  fs.writeFileSync(outputPath, lines.join("\n"), "utf8");
}

function main() {
  if (!fs.existsSync(typesPath)) {
    fail(`Types file not found: ${typesPath}`);
  }

  const content = fs.readFileSync(typesPath, "utf8");
  const tablesSection = extractTablesSection(content);
  const tableNames = parseTableNames(tablesSection);
  const keyToRelations = parseForeignKeys(tablesSection, tableNames);
  const fkMap = buildTrackedFkMap(keyToRelations);
  writeOutputFile(fkMap);

  console.log(
    `[dev-tools fk sync] Wrote ${Object.keys(fkMap).length} FK entries to ${outputPath}`,
  );
}

main();
