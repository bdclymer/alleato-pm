type ChangeRequestPreviewFields = {
  project_id?: unknown;
  title?: unknown;
  description?: unknown;
  scope?: unknown;
  type?: unknown;
  status?: unknown;
  reason?: unknown;
  origin?: unknown;
  origin_id?: unknown;
  expecting_revenue?: unknown;
  line_item_revenue_source?: unknown;
  prime_contract_id?: unknown;
};

type ReviewCardField = {
  key: string;
  label: string;
  value: string | null;
  helper: string;
  required?: boolean;
  generated?: boolean;
};

export type AssistantWriteReviewCard = {
  title: string;
  subtitle?: string;
  recordType: "change_event";
  groups: Array<{
    title: string;
    fields: ReviewCardField[];
  }>;
  notices?: Array<{
    tone: "info" | "warning";
    text: string;
  }>;
};

function formatPreviewValue(value: unknown): string | null {
  if (value == null) return null;
  if (typeof value === "string") return value.trim() || null;
  if (typeof value === "number" && Number.isFinite(value)) return String(value);
  if (typeof value === "boolean") return value ? "Yes" : "No";
  return JSON.stringify(value);
}

function guideField(name: string) {
  return [
    ...CHANGE_REQUEST_FIELD_GUIDE.requiredFields,
    ...CHANGE_REQUEST_FIELD_GUIDE.optionalFields,
    ...CHANGE_REQUEST_FIELD_GUIDE.generatedFields,
  ].find((field) => field.name === name);
}

function reviewField(
  name: string,
  key: keyof ChangeRequestPreviewFields | string,
  value: unknown,
  options: { required?: boolean; generated?: boolean } = {},
): ReviewCardField {
  const guide = guideField(name);
  return {
    key,
    label: guide?.label ?? name,
    value: formatPreviewValue(value),
    helper: guide?.description ?? "",
    required: options.required,
    generated: options.generated,
  };
}

export function buildChangeRequestReviewCard(
  fields: ChangeRequestPreviewFields,
): AssistantWriteReviewCard {
  return {
    title: "Change event draft",
    subtitle: "Review the fields before Alleato creates the record.",
    recordType: "change_event",
    groups: [
      {
        title: "General information",
        fields: [
          reviewField("projectId", "project_id", fields.project_id, {
            required: true,
          }),
          reviewField("number", "number", "Generated after confirmation", {
            generated: true,
          }),
          reviewField("title", "title", fields.title, { required: true }),
          reviewField("status", "status", fields.status),
          reviewField("description", "description", fields.description),
          reviewField("type", "type", fields.type),
          reviewField("scope", "scope", fields.scope),
          reviewField("reason", "reason", fields.reason),
        ],
      },
      {
        title: "Source and contract",
        fields: [
          reviewField("origin", "origin", fields.origin),
          reviewField("originId", "origin_id", fields.origin_id),
          reviewField(
            "primeContractId",
            "prime_contract_id",
            fields.prime_contract_id,
          ),
        ],
      },
      {
        title: "Revenue settings",
        fields: [
          reviewField(
            "expectingRevenue",
            "expecting_revenue",
            fields.expecting_revenue,
          ),
          reviewField(
            "lineItemRevenueSource",
            "line_item_revenue_source",
            fields.line_item_revenue_source,
          ),
        ],
      },
      {
        title: "Line items",
        fields: [
          {
            key: "line_items",
            label: "Line items",
            value: "Not supported by chat create yet",
            helper:
              "Use the full form after creation for budget code, vendor, commitment, cost, revenue, and non-committed cost lines.",
          },
        ],
      },
      {
        title: "Generated by Alleato",
        fields: [
          reviewField("updated_at", "updated_at", "Set at write time", {
            generated: true,
          }),
        ],
      },
    ],
    notices: [],
  };
}

function formatFieldList(
  fields: ReadonlyArray<{
    name: string;
    label: string;
    description: string;
  }>,
) {
  return fields
    .map((field) => `- ${field.name} (${field.label}): ${field.description}`)
    .join("\n");
}

